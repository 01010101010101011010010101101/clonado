import java.lang.Math;

public class OperacionCientifica {

    public static double calcularSeno(double numero) {
        return Math.sin(numero);
    }

    public static double calcularCoseno(double numero) {
        return Math.cos(numero);
    }

    public static double calcularTangente(double numero) {
        return Math.tan(numero);
    }

    public static double calcularLogaritmo(double numero) {
        return Math.log(numero);
    }

    public static double calcularRaizCuadrada(double numero) {
        return Math.sqrt(numero);
    }

    public static double elevarPotencia(double base, double exponente) {
        return Math.pow(base, exponente);
    }

    // Puedes agregar más operaciones científicas según sea necesario
}
